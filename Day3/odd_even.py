# Modulo Operator, %
	# Calculate Remainder. (e.g., 10 % 5 == 0, 10 % 3 == 1)
	# Odd-Even Check
print("Odd-Even Check")
number = int(input("ODD or EVEN >>> "))
if number % 2 == 1:
	print(f"{number} is ODD.")
else:
	print(f"{number} is EVEN.")